// 3. 核心演算法：平衡物理拆解器
window.decomposeTo12 = function(idiom) {
    let parts = idiom.split(''); // [一, 元, 復, 始] (長度 4)

    let safety = 0;
    while (parts.length < 12 && safety < 100) {
        // 找出目前零件中，還能再拆解、且結構最複雜的零件 (這裡簡單以長度模擬，或隨機輪詢)
        let splitDone = false;
        
        // 為了「平均拆解」，我們每次尋找最長的字來拆
        for (let i = 0; i < parts.length; i++) {
            let target = parts[i];
            if (window.charDecomposeMap[target]) {
                let subParts = window.charDecomposeMap[target];
                
                // 檢查拆解後總數是否會超過 12
                // 如果零件太多導致一次拆開就爆 12，我們隨機選取其中零件以維持 12 個
                if (parts.length + subParts.length - 1 <= 12) {
                    parts.splice(i, 1, ...subParts);
                    splitDone = true;
                } else {
                    // 如果 subParts 太長，只取前幾個，直到剛好 12 (物理邏輯上這表示部分零件維持合體)
                    let canTake = 12 - (parts.length - 1);
                    if (canTake > 1) {
                        let take = subParts.slice(0, canTake - 1);
                        let left = subParts.slice(canTake - 1).join(''); 
                        parts.splice(i, 1, ...take, left);
                        splitDone = true;
                    }
                }
            }
            if (parts.length === 12) break;
        }
        
        if (!splitDone) {
            // 如果字典裡沒東西拆了但還不到12個，強行對現有零件做筆畫切割 (如 "一" 變 "丶" " ")
            for (let i = 0; i < parts.length; i++) {
                if (parts[i].length > 1) continue; 
                parts.splice(i, 1, parts[i], " "); // 補空白面
                if (parts.length === 12) break;
            }
            break;
        }
        safety++;
    }

    // 最後打亂順序，平均分配到兩個骰子
    return parts.sort(() => Math.random() - 0.5);
};

// 4. 題目生成器
window.getIdiomByDay = function(dayOfYear) {
    const fullList = [...window.rawIdiomList];
    while(fullList.length < 366) fullList.push(...window.rawIdiomList);
    
    const data = fullList[(dayOfYear - 1) % fullList.length];
    
    // 確保 hint 不含謎底
    let cleanHint = data.m.replace(new RegExp(data.w, 'g'), "＊＊＊");

    return {
        id: dayOfYear,
        idiom: data.w,
        hint: cleanHint,
        example: data.e || "（暫無例句）",
        parts: window.decomposeTo12(data.w)
    };
};
