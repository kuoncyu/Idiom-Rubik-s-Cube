// 3. 核心邏輯：DFS 深度搜尋精準湊滿 12 面
// ==========================================

window.getIdiomByDay = function(dayOfYear) {
    const index = (dayOfYear - 1) % window.fullIdiomList.length;
    const idiomData = window.fullIdiomList[index];

    const parts = window.decomposeTo12(idiomData.w);

    return {
        id: dayOfYear,
        idiom: idiomData.w,
        hint: idiomData.m, 
        parts: parts
    };
}

/**
 * 智慧 DFS 拆字演算法
 * 保證結果必定是 12 個陣列，且為真實筆畫組成，絕不亂湊。
 */
window.decomposeTo12 = function(str) {
    let initial = str.split('');
    
    // 1. 使用 DFS 尋找「完美等於 12」的拆解路徑
    function searchPerfect12(currentParts) {
        if (currentParts.length === 12) return currentParts;
        if (currentParts.length > 12) return null; // 超過 12，這條路不通
        
        for (let i = 0; i < currentParts.length; i++) {
            let char = currentParts[i];
            // 優先找大部件，找不到再找微觀筆畫
            let splits = window.charMap[char] || window.strokeMap[char];
            
            if (splits && splits.length > 1) {
                let nextParts = [...currentParts];
                nextParts.splice(i, 1, ...splits); // 將原字替換為拆解後的數個部件
                
                let result = searchPerfect12(nextParts);
                if (result) return result; // 找到了完美的 12 個！
            }
        }
        return null; // 此路徑死胡同
    }
    
    let exact12 = searchPerfect12(initial);
    if (exact12) return exact12;

    // 2. 備用方案 (極端情況：如果成語筆畫真的太少，例如 "一一一一" 怎麼拆都不到 12)
    // 則退而求其次，盡最大努力拆解，直到拆無可拆。
    let parts = [...initial];
    let loop = 0;
    while(parts.length < 12 && loop < 100) {
        let splitDone = false;
        for(let i=0; i<parts.length; i++) {
            let char = parts[i];
            let splits = window.charMap[char] || window.strokeMap[char];
            
            // 只要拆了不會爆掉 12，就拆
            if(splits && parts.length + splits.length - 1 <= 12) {
                parts.splice(i, 1, ...splits);
                splitDone = true;
                break;
            }
        }
        if(!splitDone) {
            // 真的完全無法拆解了（例如剩下全是基本筆畫），則用 "丶" (點) 來補齊最後的面
            parts.push("丶"); 
        }
        loop++;
    }
    return parts.slice(0, 12);
}
