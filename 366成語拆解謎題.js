/**
 * 核心拆分函數
 */
window.decomposeTo12 = function(idiom) {
    let parts = idiom.split('');
    
    // 循環拆解直到達到 12 個部件
    let safetyCounter = 0;
    while (parts.length < 12 && safetyCounter < 50) {
        let splitOccurred = false;
        for (let i = 0; i < parts.length; i++) {
            let target = parts[i];
            // 優先找部件字典，找不到找筆畫字典
            let subParts = window.charMap[target] || window.strokeMap[target];
            
            if (subParts && subParts.length > 1) {
                // 檢查拆開後會不會總數超過 12？ 
                // 如果會超過，則嘗試只拆成 2 個，否則全部拆開
                let gain = subParts.length - 1;
                if (parts.length + gain <= 12) {
                    parts.splice(i, 1, ...subParts);
                    splitOccurred = true;
                    break; 
                }
            }
        }
        if (!splitOccurred) break; // 無法再拆了
        safetyCounter++;
    }

    // 如果還是不到 12 個面，用裝飾性筆畫或成語中的字重複拆出的筆畫補齊
    while (parts.length < 12) {
        parts.push("丶");
    }
    
    return parts.slice(0, 12);
};

