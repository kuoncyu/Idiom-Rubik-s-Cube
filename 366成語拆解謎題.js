/**
 * 核心：輪流平均拆解演算法
 */
window.decomposeTo12 = function(idiom) {
    let parts = idiom.split(''); // 起點：4 個字
    
    // 如果字太少 (通常不會發生，成語固定4字)，補齊到 4 個
    while (parts.length < 4) parts.push(" ");

    let safety = 0;
    // 循環直到湊滿 12 個部件
    while (parts.length < 12 && safety < 100) {
        let changed = false;
        
        // 每一輪都嘗試對現有的每一個部件拆一次
        for (let i = 0; i < parts.length; i++) {
            let p = parts[i];
            if (window.charMap[p]) {
                let sub = window.charMap[p];
                // 確保拆開後總數不會爆過 12 太遠
                // (如果拆開後會變成 13 個，我們就只取需要的數量，或是換下一個部件拆)
                if (parts.length + sub.length - 1 <= 12) {
                    parts.splice(i, 1, ...sub);
                    changed = true;
                    if (parts.length === 12) break;
                }
            }
        }
        
        if (!changed) {
            // 如果所有部件在字典都沒了，但還不到 12，則進行強制筆畫拆解
            for (let i = 0; i < parts.length; i++) {
                if (parts[i].length === 1 && !"一丨丿丶乙亅".includes(parts[i])) {
                    // 隨便拆出一個點
                    parts.splice(i, 1, parts[i], "丶");
                    changed = true;
                    break;
                }
            }
            if (!changed) break; // 真的極限了
        }
        safety++;
    }

    // 最終保險：如果最後還是不足 12 面 (例如 "一一一一")，補空格或裝飾點
    while (parts.length < 12) {
        parts.push(" ");
    }

    // 洗牌打亂順序，讓玩家看不出字與字的關聯
    return parts.sort(() => Math.random() - 0.5);
};

window.getIdiomByDay = function(dayOfYear) {
    const data = window.fullIdiomList[(dayOfYear - 1) % window.fullIdiomList.length];
    return {
        id: dayOfYear,
        idiom: data.w,
        hint: data.m,
        parts: window.decomposeTo12(data.w)
    };
};
