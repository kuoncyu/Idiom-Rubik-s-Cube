// 3. 核心演算法：平衡物理拆解器
// 保證：絕對不超過 12 個、耗盡字典後剩餘空位填補「空白字串」
// ==========================================
window.decomposeTo12 = function(idiom) {
    // 初始陣列：將四字成語拆成四個字 [字1, 字2, 字3, 字4]
    let parts = idiom.split('');
    let safety = 0;
    
    // 只要還不到 12 個面，就不斷尋找能拆解的字
    while (parts.length < 12 && safety < 100) {
        let splitDone = false;
        
        // 為了平均拆解，依序檢查目前的每一個部件
        for (let i = 0; i < parts.length; i++) {
            let target = parts[i];
            
            // 如果這個部件在字典裡有定義拆解法
            if (window.charDecomposeMap[target]) {
                let subParts = window.charDecomposeMap[target];
                
                // 計算如果把它拆開，總數會是多少？
                let newLength = parts.length - 1 + subParts.length;
                
                if (newLength <= 12) {
                    // 安全範圍內，將原部件替換成拆解後的多個小部件
                    parts.splice(i, 1, ...subParts);
                    splitDone = true;
                    break; // 每次拆解完跳出迴圈，重新計算長度
                } else {
                    // 如果拆下去會超過 12 個，我們只取「剛好能湊滿 12 個」的數量
                    // 其餘的部件在邏輯上保持「合體」狀態
                    let needed = 12 - parts.length + 1; 
                    if (needed > 1) {
                        let take = subParts.slice(0, needed - 1);
                        let left = subParts.slice(needed - 1).join('');
                        parts.splice(i, 1, ...take, left);
                        splitDone = true;
                        break;
                    }
                }
            }
        }
        
        // 如果整個陣列巡過一遍，發現都無法再拆解了，就結束迴圈
        if (!splitDone) {
            break;
        }
        safety++;
    }

    // 終極保險機制：如果字體太過簡單（例如「一一一一」），
    // 導致連字典都拆無可拆時，剩下的骰子面直接顯示「空白」，絕不造假筆畫！
    while (parts.length < 12) {
        parts.push(""); 
    }

    // 隨機打亂這 12 個部件的順序，準備貼到兩顆骰子上
    return parts.sort(() => Math.random() - 0.5);
};

